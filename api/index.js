// ///////////////////////////////////////////////////////////////////// Imports
// -----------------------------------------------------------------------------
import Axios from 'axios'

// ///////////////////////////////////////////////////////////////////// Exports
// -----------------------------------------------------------------------------
export default {
  // ------------------------------------------------------- Generic Get Request
  getData (url) {
    return Axios
      .get(url)
      .then((response) => {
        return response.data
      }).catch((error) => {
        return { error }
      })
  }
}
